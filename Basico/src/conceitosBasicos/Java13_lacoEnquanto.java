package conceitosBasicos;

public class Java13_lacoEnquanto {

	public static void main(String[] args) {
		
		//Variavel inteira
		int i = 0;
		
		// Execute enquanto i for menor que 10 
		while(i < 10) {
			//Exibe as informações da variavel "i"
			System.out.println(i);
			//Exibe a variavel "i" e Incrementa mais
			i++;
			
		}
		
		

	}

}
