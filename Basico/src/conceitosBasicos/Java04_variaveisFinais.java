package conceitosBasicos;

public class Java04_variaveisFinais {

	public static void main(String[] args) {
		
		//Variavel constante inteira
		final int SOBE = 0;
		final int DESCE = 1;
		System.out.println(SOBE);
		System.out.println(DESCE);
		
		//Variavel constante flutuante
		final float PI = 3.14f;
		System.out.println(PI);
		
		// Variavel constante booleana
		final boolean RUN = true;
		System.out.println(RUN);
		
		// Variavel constante caracter
		final char A = 'A';
		System.out.println(A);
		
		// Variavel constante String (Texto)
		final String CONST = "Amor para sempre";
		System.out.println(CONST);

	}

}
