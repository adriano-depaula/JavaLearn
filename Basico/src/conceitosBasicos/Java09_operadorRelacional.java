package conceitosBasicos;

public class Java09_operadorRelacional {

	public static void main(String[] args) {
		
		//Variaveis inteira
		int a = 100;
		int b = 200;
		int c = 100;
		
		//Exibe resultado "a" maior que "b"
		System.out.println(a > b);
		//Exibe resultado "a" maior ou igual a "c"
		System.out.println(a >= c);
		//Exibe resultado "a" menor que "b"
		System.out.println(a < b);
		//Exibe resultado "a" menor ou igual a "b"
		System.out.println(a <= b);
		//Exibe resultado "a" igual a "c"
		System.out.println(a == c);
		//Exibe resultado "a" diferente de "b"
		System.out.println(a != b);

	}

}
